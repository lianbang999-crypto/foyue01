def pick_daan(items, cats, kws, topn=20):
    scored = []
    for e in items:
        sc = 0
        if e['cats']:
            if e['cats'][0] in cats:
                sc += 3
            elif set(e['cats']) & set(cats):
                sc += 1
        sc += 2 * sum(1 for k in kws if k in e['text'])
        if sc >= 2:
            scored.append((sc + e['score'] * 0.05, e))
    scored.sort(key=lambda x: -x[0])
    return [e for _, e in scored[:topn]]

def pick_cbeta(works, kws, topn=15):
    scored = []
    for wid, items in works.items():
        for e in items:
            sp = T2S.convert(e['text'])
            sc = 2 * sum(1 for k in kws if k in sp)
            if sc >= 2:
                scored.append((sc + e['score'] * 0.02, e))
    scored.sort(key=lambda x: -x[0])
    return [e for _, e in scored[:topn]]

def main():
    os.makedirs(OUT, exist_ok=True)
    daan = json.load(open(os.path.join(LIB, 'data', 'daan_excerpts.json')))['items']
    cbeta = json.load(open(os.path.join(LIB, 'data', 'cbeta_excerpts.json')))

    for cid, name, verse, cats, kws in CHAPTERS:
        dd = pick_daan(daan, cats, kws)
        cc = pick_cbeta(cbeta, kws)
        parts = [f'# {cid} {name} · 材料底稿\n',
                 f'> 论偈：{verse}',
                 f'> 检索主题：{("、".join(cats)) or "（无，纯关键词）"} ｜ 关键词：{"、".join(kws)}',
                 '> 候选≠定稿。写作时回「经典原文/」「data/*.json」取全段，逐字核对后引用。\n',
                 f'## 经论疏候选（{len(cc)} 条）\n']
        for e in cc:
            head = f'（{e["head"]}）' if e.get('head') else ''
            parts.append(f'- **{e["title"]}** 段{e["paraStart"]}–{e["paraEnd"]}{head}〔CBETA {e["workId"]}〕')
            parts.append(f'  {e["text"][:100].replace(chr(10), " ")}…')
        parts.append(f'\n## 大安法师讲解候选（{len(dd)} 条）\n')
        for e in dd:
            parts.append(f'- **{e["seriesTitle"]}·{e["chapterTitle"]}** 段{e["paraStart"]}–{e["paraEnd"]}｜{"、".join(e["s1"][:4]) or (e["cats"][0] if e["cats"] else "")}')
            parts.append(f'  {e["text"][:100].replace(chr(10), " ")}…')
        with open(os.path.join(OUT, f'{cid}-{name.replace("·", "")}-材料.md'), 'w', encoding='utf-8') as f:
            f.write('\n'.join(parts))
        print(f'{cid} {name}: 经论{len(cc)} + 讲解{len(dd)}')

if __name__ == '__main__':
    main()
